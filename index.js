
class Breakfast {
    constructor(food, drink){
        this.food = food ;
        this.drink = drink ;
    }
}   

class Lunch {
    constructor (salad, soup, drink){
        this.salad = salad;
        this.soup = soup;
        this.drink = drink;
    }
}

class Dinner {
    constructor(salad, soup,entree,_dessert){
    this._dessert = _dessert ;
    this.entree = entree;
    this.soup = soup;
    this.salad = salad;
 }
}